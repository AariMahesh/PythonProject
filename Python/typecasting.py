#type casting
'''
Converting all the type of data into int using int():
'''
#float to int
a=1234.567
b=int(a)
print(b)
print(type(b))
#bool to int
a=True
b=int(a)
print(b)
print(type(b))
#str to int
a='mahi'
b=int(a)
print(b)
print(type(b))
#complex to int(cant be done)
a=12+10j
#b=int(a)#error
b=imag(a)
print(b)
print(type(b))
